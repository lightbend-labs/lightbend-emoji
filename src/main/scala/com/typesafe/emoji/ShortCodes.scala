package com.lightbend.emoji

/**
 * A emoji to shortcode mapping.  This is a class that should be declared and used
 * as an implicit value, so that shortcode mappings don't have to be global across
 * an application.
 *
 * "import ShortCodes.Defaults._" to import the default shortcode mapping.
 * "import ShortCodes.Implicits._" to enrich Emoji and String with shortcode methods.
 */
class ShortCodes(template: Option[ShortCodes] = None) {

  private val emojiToShortCodes = collection.mutable.Map[Emoji, collection.Set[String]]()

  private val shortCodeToEmoji = collection.mutable.Map[String, Emoji]()

  template.foreach { t =>
    for ((emoji, shortCodes) <- t.emojiToShortCodes) {
      shortCodes.foreach(code => entry(emoji, code))
    }
  }

  /**
   * Defines a mapping between an emoji and a short code.  Emojis may have multiple
   * short code mappings.
   */
  def entry(emoji: Emoji, shortCode: String): Unit = {
    emojiToShortCodes.get(emoji) match {
      case Some(shortCodes) =>
        emojiToShortCodes += (emoji -> (shortCodes + shortCode))
      case None =>
        emojiToShortCodes += (emoji -> Set(shortCode))
    }

    shortCodeToEmoji += (shortCode -> emoji)
  }

  /**
   * Returns the short codes for this emoji.
   */
  def shortCodes(emoji: Emoji): Option[collection.Set[String]] = {
    emojiToShortCodes.get(emoji)
  }

  /**
   * Returns Some(emoji) if a short code is defined, None otherwise
   */
  def emoji(shortCode: String): Option[Emoji] = {
    shortCodeToEmoji.get(shortCode)
  }

  /**
   * Returns the set of emojis that have short codes.
   */
  def emojis: collection.Set[Emoji] = {
    emojiToShortCodes.keySet
  }

  /**
   * Returns the set of short codes mapped to emojis.
   */
  def shortCodes: collection.Set[String] = {
    shortCodeToEmoji.keySet
  }

  /**
   * Removes emoji from the shortcodes mapping.  This removes all the codes
   * that map to the emoji as well.
   */
  def removeEmoji(emoji: Emoji): Unit = {
    emojiToShortCodes.remove(emoji).foreach { shortCodes =>
      shortCodes.foreach { shortCode =>
        shortCodeToEmoji.remove(shortCode)
      }
    }
  }

  /**
   * Removes a shortcode from the mapping.  This does not remove the emoji.
   */
  def removeCode(shortCode: String): Unit = {
    shortCodeToEmoji.remove(shortCode).foreach { emoji =>
      emojiToShortCodes.get(emoji).map { codes =>
        val set = codes - shortCode
        if (set.isEmpty) {
          emojiToShortCodes.remove(emoji)
        } else {
          emojiToShortCodes.put(emoji, set)
        }
      }
    }
  }

  /**
   * Completely removes the emoji and shortcodes from the mapping.
   */
  def clear(): Unit = {
    emojiToShortCodes.clear()
    shortCodeToEmoji.clear()
  }

}

