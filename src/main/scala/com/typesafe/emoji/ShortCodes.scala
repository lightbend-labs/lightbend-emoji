object ShortCodes {

  /**
   * Returns the in-scope implicit shortcodes mapping.
   */
  def current(implicit shortCodes: ShortCodes): ShortCodes = {
    shortCodes
  }

  /**
   * Maps short codes onto Emoji and String, so you can say "+1".emoji and thumbsUpEmoji.shortCodes.
   */
  object Implicits {

    implicit class ShortCodeString(shortCode: String) {
      def emoji(implicit shortCodes: ShortCodes): Emoji = {
        shortCodes.emoji(shortCode).getOrElse {
          throw new EmojiNotFound("No emoji found for short code")
        }
      }
    }

    implicit class EmojiShortCodes(emoji: Emoji) {
      def shortCodes(implicit shortCodes: ShortCodes): Option[collection.Set[String]] = {
        shortCodes.shortCodes(emoji)
      }
    }

